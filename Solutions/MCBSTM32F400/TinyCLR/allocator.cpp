#include <stddef.h>
#include <tinyhal.h>

////////////////////////////////////////////////////////////////////////////////


void *operator new( size_t n )
{
    return malloc( n );
}

void *operator new[]( size_t n )
{
    return malloc( n );
}

void operator delete( void* p )
{
    return free( p );
}

void operator delete[]( void* p )
{
    return free( p );
}

////////////////////////////////////////////////////////////////////////////////

